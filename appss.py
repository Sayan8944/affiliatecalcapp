# --------------------------------------------
# 4) Slider 1: Number Of Retailers (1 ‚Üí 200, default 100)
# --------------------------------------------
num_retailers = st.slider(
    label="Number Of Retailers",
    min_value=1,
    max_value=200,
    value=100,
    step=1,
)

# --------------------------------------------
# 5) Slider 2: Avg Number Of Purchases Per Mo. (1 ‚Üí 200, default 50)
# --------------------------------------------
avg_purchases_per_month = st.slider(
    label="Avg Number Of Purchases Per Mo.",
    min_value=1,
    max_value=200,
    value=50,
    step=1,
)

# --------------------------------------------
# 6) Numeric Input: Your Affiliate Sale ($49.00 default)
# --------------------------------------------
affiliate_sale = st.number_input(
    label="Your Affiliate Sale ($USD)",
    min_value=0.00,
    max_value=10000.00,
    value=49.00,
    step=1.00,
    format="%.2f",
)

# --------------------------------------------
# 7) Compute Monthly Profit
#    Formula: Number_Of_Retailers √ó Avg_Purchases_Per_Month √ó Affiliate_Sale
# --------------------------------------------
